package JavaClass7;

public class DoWhileLoop {

	public static void main(String[] args) {
		// Do While Loop
		//
		
		
		int a=1;
		while(a>=3) {
			System.out.println("hi");
			a++;
		}

		System.out.println("----------CODE USING FO EHILR LOOP--------");
		
		int b=1;
		do {
			System.out.println("hello");
			
			b++;
			
		}while(b>=3);
		
		
		
	}

}
